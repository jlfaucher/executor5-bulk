#ifndef ActivityState_Included
#define ActivityState_Included

typedef enum
{
    NEW_ACTIVITY,                   // thread under construction
    ACTIVATED,                      // thread is active, not terminated
    QUIESCED,                       // thread is attached, but not entered
    PROCESSING_EXIT,                // thread is processing an exit call.
    PROCESSING_METHOD,              // thread is processing a method call,
    PROCESSING_CALL,                // thread is processing an external method call
    WAITING_FOR_ACTIVATION,         // thread is active, but non-dispatched,
    POOLED,                         // thread is in a thread pool
    TERMINATE_THREAD,               // termination has been requested
    TERMINATING_ACTIVITY,           // termination in process
    RUNNING_ACTIVITY                // activity doing real work
} ActivityState;

#endif
